list<list<int>> solve(int start, int end, int length){
    list<list<int>> v;
    if (length > end - start) return v;
    if (length == 0){
        v.push_back(list<int>{});
        return v;
    }

    for (auto i: solve(start+1, end, length-1)){
        list<int> elem = i;
        elem.push_front(start);
        v.push_back(elem);
    }

    for (auto i: solve(start+1, end, length)){
        v.push_back(i);
    }
    return v;
}

