void solve(){
    ll n;
    cin >> n;
    if (n == 2){cout << -1 << endl; return;}

    ll arr[n*n+2];
    
    for (ll i=1; i<=n*n;i++){
        arr[i-1] = (i % 2 == 0) ? (i+n*n + 1) / 2 : (i+1)/2;
    }

    for (int i=0; i<n*n;i++){
        if ((i / n) % 2 == 0) cout << arr[i];
        else cout << arr[i+n - 1 - 2 * (i % n)];
        cout << " ";
        if (i % n == n-1) cout << endl;
    }

    return;
}

